number = 5
print(f"number = {number:0b}")

number = 0b101
print(f"number = {number}")
print(f"number = {number:0b}")

print(5 & 6)  # 4 101 & 110 -> 100 = 4
print(5 | 6)  # 101 | 110 -> 111 = 7
print(5 ^ 6)  # 101 ^ 110 -> 011 = 3

print(~5)  # -(x+1)

print(5 << 1)  # 101 << 1 -> 1010 = 10
print(5 >> 1)  # 101 >> 1 -> 010 = 2
